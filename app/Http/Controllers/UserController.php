    public function sign_in(){
        $email = Request('Email');
        $password = Request('Password');
        $user = User::where('email',$email)->get();
        if($user[0]->password == $password)
        Auth::login($user[0]);
        else
            return "data didn't match our record";

        return "Authenticated successfully";
    }


